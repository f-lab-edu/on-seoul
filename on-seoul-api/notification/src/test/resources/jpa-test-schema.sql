-- в”Җв”Җ service_change_log (H2 вҖ” notification module test) в”Җв”Җв”Җв”Җв”Җв”Җ
CREATE TABLE IF NOT EXISTS service_change_log
(
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    collection_id  BIGINT       NOT NULL DEFAULT 0,    -- prod: FK, H2: DEFAULT 0 н—Ҳмҡ©
    service_id     VARCHAR(30)  NOT NULL,
    change_type    VARCHAR(20)  NOT NULL,
    field_name     VARCHAR(100),
    old_value      TEXT,
    new_value      TEXT,
    changed_at     TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT NOW()
);

-- в”Җв”Җ public_service_reservations (H2 вҖ” notification module test, JOIN лҢҖмғҒ) в”Җв”Җ
