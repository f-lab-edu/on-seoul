-- в”Җв”Җ notification_dispatches (H2) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
-- ADR-0004: change_log_id м ңкұ°. batch_id, attempt_count м¶”к°Җ.
CREATE TABLE IF NOT EXISTS notification_dispatches
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    batch_id         BIGINT        NOT NULL REFERENCES notification_batch (id),
    subscription_id  BIGINT        NOT NULL REFERENCES notification_subscriptions (id),
    -- migration 11: мӢңм җ кё°л°ҳ м•ҢлҰј нҠёлҰ¬кұ°(лӘЁлҚё B)
    trigger_type     VARCHAR(20)   NOT NULL DEFAULT 'CHANGE',
    dispatch_date    DATE,
    service_id       VARCHAR(30),
    status           VARCHAR(10)   NOT NULL DEFAULT 'PENDING',
    sent_at          TIMESTAMP,
    generated_title  VARCHAR(200),
    generated_body   CLOB,
    template_source  VARCHAR(10),
    last_error       CLOB,
    attempt_count    INT           NOT NULL DEFAULT 0,
    -- JSONB in PostgreSQL; H2 test schema declares as VARCHAR (filter/channelsмҷҖ лҸҷмқј нҢЁн„ҙ).
    -- @JdbcTypeCode(SqlTypes.JSON) String л°”мқёл”©кіј нҳёнҷҳ. NULL н—Ҳмҡ©.
    notification_payload VARCHAR(8000),
    created_at       TIMESTAMP     NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMP     NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_nd_batch_subscription UNIQUE (batch_id, subscription_id),
    CONSTRAINT chk_nd_status CHECK (status IN ('PENDING', 'SUCCESS', 'FAILED', 'DEAD')),
    CONSTRAINT chk_nd_trigger_type CHECK (trigger_type IN ('CHANGE', 'OPEN_DAY', 'BEFORE_RECEIPT_D1', 'DEADLINE_DDAY')),
    CONSTRAINT chk_nd_template_source CHECK (template_source IN ('AI', 'FALLBACK') OR template_source IS NULL)
);

