-- в”Җв”Җ notification_subscriptions (H2) в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
CREATE TABLE IF NOT EXISTS notification_subscriptions
